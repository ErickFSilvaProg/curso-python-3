# Concatenação: Utiliza-se o sinal de adição.
concatenacao = "A" + "B" + "C"
print(concatenacao)

nome_completo = "Erick" + " " + "Ferreira da Silva"
print(nome_completo)

nome_idade = "Erick tem " + str(38) + " anos"
