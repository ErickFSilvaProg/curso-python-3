"""
    —→ Função print()
        ↪ Recebe argumentos para serem exibidos ou processados.
    
    —→ O Python diferencia letras maiúsculas de letras minúsculas.
"""

# Argumentos não nomeados:
print('- Argumentos não nomeados:')
print(12,34)
print(56, 78)
print()


# Argumentos nomeados:
#   sep: representa um separador.
#   end: representa o final do print.
print('- Argumentos nomeados:')
print(12,34,sep=' ↔ ')
print(56,78,sep=" • ")
print()