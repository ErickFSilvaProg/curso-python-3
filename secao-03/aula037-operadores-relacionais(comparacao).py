"""
- Operadores relacionais (comparação):

OPERADOR        Significado Exemplo       (True)

>               maior                     2 > 1
>=              maior ou igual            2 >= 2
<               menor                     1 < 2
<=              menor igual               2 <= 2
==              igual                     'a' == 'a'
!=              deferente                 'a' != 'b'

"""

maior = 2 > 1
print("2 > 1")
print(maior)
print()

maior_ou_igual = 2 >= 2
print("2 >= 2")
print(maior_ou_igual)
print()

menor = 1 < 2
print("1 < 2")
print(menor)
print()

menor_ou_igual = 2 <= 2
print("2 <= 2")
print(menor_ou_igual)
print()

igual = "a" == "a"
print("'a' == 'a'")
print(igual)
print()

diferente = "a" != "b"
print("'a' != 'b'")
print(diferente)
print()