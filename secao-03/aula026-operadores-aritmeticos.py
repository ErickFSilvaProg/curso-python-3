"""
Quando realizamos uma operação entre um número inteiro e um número com ponto flutuante, o resultado sempre será um valor com ponto flutuante.
"""

# Exponenciação:
exponenciacao = 10 ** 2
print("Exponenciação:", exponenciacao)

# Multiplicação:
multiplicacao = 10 * 2
print("Multiplicação:", multiplicacao)

# Divisão: Sempre retornará um float.
divisao = 10 / 2
print("Divisão:", divisao)

# Divisão inteira: Sempre retornará um valor inteiro, mesmo que o resultado seja do tipo seja float.
divisao_inteira = 10 // 3
print("Divisão inteira:", divisao_inteira)

divisao_inteira = 10 // 2
print("Divisão inteira:", divisao_inteira)

# Módulo: Imprime o resto da divisão.
modulo = 55 % 2
print("Módulo:", modulo)

# Adição:
adicao = 10 + 10
print("Adição:", adicao)

# Subtração:
subtracao = 10 - 5
print("Subtração:", subtracao)
print()