# Adição:
adicao = 10 + 10
print('Adição:', adicao)

# Subtração:
subtracao = 10 - 5
print('Subtração:', subtracao)

# Multiplicação:
multiplilcacao = 10 * 10
print('Multiplicação:', multiplilcacao)

# Divisão: sempre retornará um float.
divisao = 10 / 3
print('Divisão:', divisao)

# Divisão inteira: sempre retornará um valor inteiro, mesmo com o tipo flutuante.
divisao_inteira = 10 // 3
print('Divisão inteira', divisao_inteira)

# Exponenciação:
exponenciacao = 2 ** 10
print('Exponenciação:', exponenciacao)

# Módulo: imprime o resto da divisão
modulo = 55 % 2
print('Módulo:', modulo)